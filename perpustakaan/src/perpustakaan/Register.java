    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
        String nisstring = NisField.getText();
        
        String username = UsernameField.getText();
        String password = PasswordField.getText();
        String status =(String)StatusField.getSelectedItem();
        String alamat = AlamatField.getText();
        String notelp = NotelpField.getText();
        
        String ceknis = NisField.getText().trim();
        String cekusername = UsernameField.getText().trim();
        String cekpassword = PasswordField.getText().trim();
        String cekalamat = AlamatField.getText().trim();
        String ceknotelp = NotelpField.getText().trim();
        
        if (ceknis.isEmpty() || cekusername.isEmpty()|| cekpassword.isEmpty() || cekalamat.isEmpty() || ceknotelp.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Kolom tidak boleh kosong");
                return; 
            }
        
        try
        {
        int nis = Integer.parseInt(nisstring);
        Class.forName("java.sql.DriverManager");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_perpus","root", "");
        Statement stmt = con.createStatement();
        
        String q="insert into users (`nis`, `username`, `password`, `alamat`, `notelp`, `status`) values ('"+nis+"', '"+username+"', '"+password+"','"+alamat+"','"+notelp+"', '"+status+"')";
        stmt.executeUpdate(q);
        
        JOptionPane.showMessageDialog(this, "User Berhasil Ditambahkan");
        NisField.setText("");
        UsernameField.setText("");
        PasswordField.setText("");
        AlamatField.setText("");
        NotelpField.setText("");
        }
        catch (Exception x)
    {
    JOptionPane.showMessageDialog(this,x.getMessage());
    }        
    }//GEN-LAST:event_RegisterButtonActionPerformed

    
