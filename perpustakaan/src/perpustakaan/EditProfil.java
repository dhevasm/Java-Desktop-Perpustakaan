    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            String username = UsernameField.getText();
            String alamat = AlamatField.getText();
            String notelp = NotelpField.getText();
            
            String cekusername = UsernameField.getText().trim();
            String cekalamat = AlamatField.getText().trim();
            String ceknotelp = NotelpField.getText().trim();
            
            if (cekusername.isEmpty()|| cekalamat.isEmpty() || ceknotelp.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Kolom tidak boleh kosong");
                return; 
            }
            
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_perpus", "root", "");
            String sql = "UPDATE users SET username = ?, alamat = ?, notelp = ? WHERE nis = ?";
            PreparedStatement statement = connection.prepareStatement(sql);
            statement.setString(1, username);
            statement.setString(2, alamat);
            statement.setString(3, notelp);
            statement.setInt(4, userid);
            
            int rowsUpdated = statement.executeUpdate();
            
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Profil berhasil diupdate");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal melakukan update Profil");
            }
            connection.close();
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

