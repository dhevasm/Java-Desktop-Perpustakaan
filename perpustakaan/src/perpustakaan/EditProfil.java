     private void displayData() {
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_perpus", "root", "");

            String sql = "SELECT * FROM users  WHERE nis = ?";
            PreparedStatement statement = connection.prepareStatement(sql);
            statement.setInt(1, userid); 
            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                String username = resultSet.getString("username");
                String alamat = resultSet.getString("alamat");
                String notelp = resultSet.getString("notelp");
                UsernameField.setText(username);
                AlamatField.setText(alamat);
                NotelpField.setText(notelp);
                
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
            }

            connection.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

