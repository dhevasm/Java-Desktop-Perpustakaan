    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
       jRadioButton1.setActionCommand("user");
       jRadioButton2.setActionCommand("admin");
       String username = UsernameField.getText();
       String password = PasswordField.getText();
       String status = StatusInput.getSelection().getActionCommand();
       String sql = "select * from users where username=? and password=? and status=?";
 
       try{  
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_perpus", "root", "");
            pst=con.prepareStatement(sql);
            pst.setString(1,username);
            pst.setString(2,password);
            pst.setString(3,status);
            rs = pst.executeQuery();
            if(rs.next())
            {
               if("admin".equals(status))
                    new Dashboard_Admin().setVisible(true);
                else 
                    new Dashboard().setVisible(true);
                    EditProfil.userid =  rs.getInt("nis");                   
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "username and password do not matched");
            }
        }
        catch(SQLException | HeadlessException ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

